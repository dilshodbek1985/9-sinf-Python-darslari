# Ikki qayiq turg'un suvda bir - biriga tomon 4 km/soat va 2 km/soat tezlik bilan suzmoqda.
# Ular orasidagi masofa 24 km bo'lsa:
# 1) Ular qancha vaqtdan keyin uchrashadi?
# Masalaningning formulasi t = s / (v1 + v2)
# Javob
v1 = 4
v2 = 2
s = 24
t = s / (v1 + v2)
print(t)

# 2) Qancha vaqtdan keyin ular orasidagi masofa 12 km tashkil etadi?
